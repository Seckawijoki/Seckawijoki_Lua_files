local function CompressSeatInfoTable(tSeatInfo)
    print(tostring(tSeatInfo), tostring(tSeatInfo.skin))
    print(tSeatInfo == nil or tSeatInfo.skin == nil)
    if tSeatInfo == nil or tSeatInfo.skin == nil then return nil end
    local tNewSeatInfo = {}
    local tNewOutterSkin = {}
    local tOutterSkin = tSeatInfo.skin
    tNewSeatInfo["skin"] = tNewOutterSkin
    for k, v in pairs(tOutterSkin) do 
        local tInnerSkin = v.skin;
        tNewOutterSkin[k] = {}
        if tInnerSkin then 
            local tNewInnerSkin = {}
            tNewOutterSkin[k]["skin"] = tNewInnerSkin
            if tInnerSkin.ModelID then
                tNewInnerSkin["ModelID"] = tInnerSkin.ModelID
            end
            if tInnerSkin.Data then
                tNewInnerSkin["Data"] = tInnerSkin.Data
            end
        end
    end
    tNewSeatInfo["texture"] = tSeatInfo.texture
    return tNewSeatInfo
end

